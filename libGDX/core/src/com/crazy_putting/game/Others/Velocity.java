public class Velocity {
    public float x;
    public float y;
    public float speed;
    public float angle;

    public Velocity(){

    }

    public Velocity(float x, float y, float speed, float angle){
        this.x = x;
        this.y = y;
        this.speed = speed;
        this.angle = angle;
    }
}
